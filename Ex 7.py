'''Faça um código que receba o valor da base e da altura de um triângulo e
calcule sua área. usando a fórmula A = (base x Altura ) /2'''
base = float(input("Digite o número da BASE do TRIÂNGULO: "))
altura = float(input("Digite o número da ALTURA do TRIÂNGULO: "))
a = (base*altura)/2
print(f"A aréa do triângulo é [{a}cm²]")
